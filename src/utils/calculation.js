const plus = (a, b) => {
    return a+b
}

const minus = (a, b) => {
    return a-b
}

const multiply = (a, b) => {
    return a*b
}

const divide = (a, b) => {
    return a/b
}

const power = (a, b) => {
    return a**b
}


export default {
    plus,
    minus,
    multiply,
    divide,
    power,
}
